package Final;

class Smoker {
    private String name;
    private String ingredientHave;

    public Smoker(String name, String ingredientHave) {
        this.name = name;
        this.ingredientHave = ingredientHave;
    }

    public String getName() {
        return name;
    }
    public String getIngredientHave() {
        return ingredientHave;
    }
}
